class Operators {

    private var num1 = 22
    private var num2 = 44

    fun multiplication(){
        val multi = num1 * num2
        println("The multiplication of num1 and num2 is $multi")
    }

    fun addition(){
        val add = num1 + num2
        println("The Addition of num1 and num2 is $add")
    }

    fun division(){
        val divide = num1 / num2
        println("The Division of num1 from num2 is $divide")
    }

    fun average(){
        val avg = (num1 + num2) / 2
        println("The Division of num1 from num2 is $avg")
    }



}